// zh / ar kept as aliases to en for now — the picker only offers en/es anyway.
const zh: Dict = en;
const ar: Dict = en;
const DICTS: Record<LangCode, Dict> = { en, es, zh, ar };

export function t(lang: LangCode, key: Key, vars?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key] ?? en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

export type I18nKey = Key;
